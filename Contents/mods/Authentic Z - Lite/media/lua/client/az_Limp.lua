local Commands = {};
Commands.AuthenticLimp = {};


local function isLimping()
	if getPlayer():getVariableFloat("WalkInjury", 0) ~= 0 then return true end
end

local function isHasCrutch(player)
    local player = getPlayer()
    local crutch = nil
    local wornItems = player:getWornItems()
        for i = 1, wornItems:size() do
            crutch = wornItems:get(i - 1):getItem();
            if crutch and (instanceof(crutch, "Clothing") or crutch:IsClothing() or crutch:getCategory() == "Clothing") then
                if crutch:getModule() == "AuthenticZClothing" and crutch:getModData()['isAuthenticZCrutch'] then
                    return true
                end
            end
        end		
end



Events.OnPlayerUpdate.Add(function(player)
    if isLimping() then
        if player:getVariableBoolean('isDontLimp') == false then
            player:setVariable('isDontLimp', 'true');
            if isClient() then
                sendClientCommand('AuthenticLimp', 'isDontLimp', {isDontLimp = true})
            end
        end
    else
        if player:getVariableBoolean('isDontLimp') == true then
            player:setVariable('isDontLimp', 'false');
            if isClient() then
                sendClientCommand('AuthenticLimp', 'isDontLimp', {isDontLimp = false})
            end
        end
    end
end)


Commands.Reanimation.isDontLimp = function(args)
    local source = getPlayer();
    local player = getPlayerByOnlineID(args.id)

    if source ~= player then
        if args.isDontLimp then
            player:setVariable('isDontLimp', 'true');
        else
            player:setVariable('isDontLimp', 'false');
        end
    end
end

Events.OnServerCommand.Add(function(module, command, args)
	if Commands[module] and Commands[module][command] then
		Commands[module][command](args)
	end
end)


